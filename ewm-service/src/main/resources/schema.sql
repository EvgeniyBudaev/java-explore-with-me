CREATE TABLE IF NOT EXISTS CATEGORIES
(
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR UNIQUE NOT NULL
);

CREATE TABLE IF NOT EXISTS USERS
(
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    email VARCHAR UNIQUE NOT NULL,
    name VARCHAR NOT NULL
);

CREATE TABLE IF NOT EXISTS LOCATIONS
(
    lat REAL NOT NULL,
    lon REAL NOT NULL,
    PRIMARY KEY (lat, lon)
);

CREATE TABLE IF NOT EXISTS COMPILATIONS
(
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    pinned BOOLEAN,
    title VARCHAR NOT NULL
);

CREATE TABLE IF NOT EXISTS EVENTS
(
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    annotation VARCHAR NOT NULL,
    category_id INTEGER REFERENCES categories(id),
    description VARCHAR NOT NULL,
    event_date TIMESTAMP NOT NULL,
    initiator_id INTEGER REFERENCES users(id) ON DELETE CASCADE,
    lat REAL NOT NULL,
    lon REAL NOT NULL,
    paid BOOLEAN DEFAULT FALSE NOT NULL,
    participant_limit INTEGER DEFAULT 0 NOT NULL,
    request_moderation BOOLEAN DEFAULT TRUE NOT NULL,
    title VARCHAR NOT NULL,
    state VARCHAR NOT NULL,
    created TIMESTAMP NOT NULL,
    published TIMESTAMP,
    views INTEGER DEFAULT 0 NOT NULL,
    compilation_id INTEGER REFERENCES compilations(id) ON DELETE SET NULL,
    FOREIGN KEY (lat, lon) REFERENCES LOCATIONS (lat, lon)
);

CREATE TABLE IF NOT EXISTS PARTICIPATION_REQUESTS
(
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    event_id INTEGER REFERENCES events(id) ON DELETE CASCADE,
    requester_id INTEGER REFERENCES users(id) ON DELETE CASCADE,
    status VARCHAR NOT NULL,
    created TIMESTAMP NOT NULL
);